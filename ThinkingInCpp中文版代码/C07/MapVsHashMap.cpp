// hash_map头文件不是标准C++ STL的一部分，
// 它是一个扩展，只在SGI STL中提供（包含在dmc发行版中）。
// 可以手动添加这些头文件。
// hash_map已在现代C++中移除，应替换为unordered_map。
//{-bor}{-msc}{-g++}{-mwcc} 
#include <hash_map>
#include <iostream>
#include <map>
#include <ctime>
using namespace std;

int main() {
    hash_map<int, int> hm;
    map<int, int> m;
    clock_t ticks = clock();
    for(int i = 0; i < 100; i++)
        for(int j = 0; j < 1000; j++)
            m.insert(make_pair(j,j));
    cout << "map插入: " << clock() - ticks << endl;

    ticks = clock();
    for(int i = 0; i < 100; i++)
        for(int j = 0; j < 1000; j++) 
            hm.insert(make_pair(j,j));
    cout << "hash_map插入: " << clock() - ticks << endl;

    ticks = clock(); 
    for(int i = 0; i < 100; i++) 
        for(int j = 0; j < 1000; j++) 
            m[j]; 
    cout << "map::operator[] 查找: " << clock() - ticks << endl;

    ticks = clock(); 
    for(int i = 0; i < 100; i++) 
        for(int j = 0; j < 1000; j++) 
            hm[j]; 
    cout << "hash_map::operator[] 查找: " << clock() - ticks << endl;

    ticks = clock(); 
    for(int i = 0; i < 100; i++) 
        for(int j = 0; j < 1000; j++) 
            m.find(j); 
    cout << "map::find() 查找: " << clock() - ticks << endl;

    ticks = clock(); 
    for(int i = 0; i < 100; i++) 
        for(int j = 0; j < 1000; j++) 
            hm.find(j); 
    cout << "hash_map::find() 查找: " << clock() - ticks << endl; 
} 
